    public void IsiTabel(){
        DefaultTableModel tabeluser = new DefaultTableModel();
        
        tabeluser.addColumn("ID User");
        tabeluser.addColumn("Nama");
        tabeluser.addColumn("Password");
        try{
            objUser.AksesData();
            objUser.TampilData();
            
            Statement stat1 = objUser.conn.createStatement();
            ResultSet res1 = stat1.executeQuery(objUser.sql);
            while (res1.next()){
                tabeluser.addRow(new Object[]{res1.getString(1), res1.getString(2), res1.getString(3)});
            }
            jTable.setModel(tabeluser);
            AturKolom();
        }catch (Exception e){
        }
    }
    
    public void SimpanData(){
        if(txtIdpengguna.getText().equals("")||txtNama.getText().equals("")||txtPassword.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Semua Data Harus Diisi!!!");
        }
        else{
            try{
                objUser.AksesData();
                objUser.Idpengguna = txtIdpengguna.getText();
                objUser.Nama = txtNama.getText();
                objUser.Password = txtPassword.getText();
                
                objUser.Simpan();
                
                objUser.res.executeUpdate(objUser.sql);
                objUser.conn.close();
                BersihIsian();
                IsiTabel();
                JOptionPane.showMessageDialog(null, "Data User Dengan ID " + objUser.Idpengguna + " Berhasil Disimpan");
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Proses penyimpanan gagal/koneksi gagal");
                System.out.println(e.getMessage());
            }
        }
    }
    
