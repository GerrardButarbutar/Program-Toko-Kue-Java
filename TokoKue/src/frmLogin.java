    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try{
            objLogin.AksesData();
            objLogin.Nama=txtNama.getText();
            objLogin.Password=txtPassword.getText();
            objLogin.Masuk();
            
            objLogin.sql="select * from tbluser where nama_user = '"+objLogin.Nama+"' and password='"+objLogin.Password+"'";
            Statement stat=objLogin.conn.createStatement();
            
            ResultSet res=stat.executeQuery(objLogin.sql);
            
            if(res.next()){
                JOptionPane.showMessageDialog(null, "Selamat Datang Admin " + objLogin.Nama + "\nSelamat Bekerja!!!");
                frmMenu menu = new frmMenu();
                dispose();
                menu.show();
            }
            else {
                JOptionPane.showMessageDialog(null, "Nama Atau Password Yang Dimasukkan Salah");
            }
        }
        catch(Exception e){            
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void txtNamaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNamaKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()== KeyEvent.VK_ENTER)
        {
            if (txtNama.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Masukkan Nama Pengguna","Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
                txtNama.requestFocus();
            }
            else
            {
                txtPassword.requestFocus();
            }
        }
    }//GEN-LAST:event_txtNamaKeyPressed

    private void txtPasswordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPasswordKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()== KeyEvent.VK_ENTER){
            if (txtPassword.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Masukkan Password","Informasi",
                JOptionPane.INFORMATION_MESSAGE);
                txtPassword.requestFocus();
            }
            else{
                btnLogin.requestFocus();
            }
        }
    }//GEN-LAST:event_txtPasswordKeyPressed

