    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(txtIdkue.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Masukkan ID Kue");
        }
        else{
            try{
                objKue.AksesData();
                objKue.Idkue = txtIdkue.getText();
                objKue.Namakue = txtNamakue.getText();
                objKue.Harga = txtHarga.getText();
                
                objKue.Edit();
                objKue.res.executeUpdate(objKue.sql);
                objKue.conn.close();
                BersihIsian();
                IsiTabel();
                AutoId();
                JOptionPane.showMessageDialog(null, "Data Kue Dengan ID " + objKue.Idkue + " Berhasil Diupdate");
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Proses penyimpanan gagal/koneksi gagal");
                
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

