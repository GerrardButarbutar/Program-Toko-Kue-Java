    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:
        JasperReport jasRep;
        JasperPrint jasPri;
        Map < String, Object> Idtransaksi = new HashMap<>();
        JasperDesign jasDes;
        try{
            objCetakReport.AksesData();
            File report1 = new File("../src/rptTransaksi.jrxml");
            jasDes = JRXmlLoader.load(report1);
            Idtransaksi.clear();
            objCetakReport.Kode1 = cboId1.getSelectedItem().toString().substring(0,4);
            objCetakReport.Kode2 = cboId2.getSelectedItem().toString().substring(0,4);
            Idtransaksi.put("mIdtransaksi", objCetakReport.Kode1);
            Idtransaksi.put("nIdtransaksi", objCetakReport.Kode2);
            jasRep = JasperCompileManager.compileReport(jasDes);
            jasPri = JasperFillManager.fillReport(jasRep, Idtransaksi, objCetakReport.conn);
            JasperViewer.viewReport(jasPri, false);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnCetakActionPerformed

